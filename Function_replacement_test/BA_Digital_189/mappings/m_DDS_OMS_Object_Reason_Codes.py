# COMMAND ----------
# DBTITLE 1, OMS_OBJECT_REASON_CODES


spark.sql("""MERGE INTO OMS_OBJECT_REASON_CODES AS TARGET
USING
  UPS_INS_UPD_7 AS SOURCE ON TARGET.OMS_REASON_CODES_ID = SOURCE.REASON_CODES_ID
  WHEN MATCHED
  AND SOURCE.UPDATE_STRATEGY_FLAG = "DD_UPDATE" THEN
UPDATE
SET
  TARGET.OMS_REASON_CODES_ID = SOURCE.REASON_CODES_ID,
  TARGET.OMS_REASON_CODES_GROUP_ID = SOURCE.REASON_CODES_GROUP_ID,
  TARGET.OMS_REASON_CD = SOURCE.REASON_CODE,
  TARGET.OMS_REASON_TYPE = SOURCE.REASON_TYPE,
  TARGET.COMMENTS = SOURCE.COMMENTS,
  TARGET.REF_FIELD_1 = SOURCE.REF_FIELD_1,
  TARGET.REF_FIELD_2 = SOURCE.REF_FIELD_2,
  TARGET.ACTIVE_FLAG = SOURCE.ACTIVE,
  TARGET.OMS_CREATED_TSTMP = SOURCE.CREATED_DTTM,
  TARGET.OMS_LAST_UPDATED_TSTMP = SOURCE.LAST_UPDATED_DTTM,
  TARGET.UPDATE_TSTMP = SOURCE.UPDATE_TSTMP_exp,
  TARGET.LOAD_TSTMP = SOURCE.LOAD_TSTMP_exp
  WHEN MATCHED
  AND SOURCE.UPDATE_STRATEGY_FLAG = "DD_DELETE"
  AND TARGET.OMS_REASON_CODES_GROUP_ID = SOURCE.REASON_CODES_GROUP_ID
  AND TARGET.OMS_REASON_CD = SOURCE.REASON_CODE
  AND TARGET.OMS_REASON_TYPE = SOURCE.REASON_TYPE
  AND TARGET.COMMENTS = SOURCE.COMMENTS
  AND TARGET.REF_FIELD_1 = SOURCE.REF_FIELD_1
  AND TARGET.REF_FIELD_2 = SOURCE.REF_FIELD_2
  AND TARGET.ACTIVE_FLAG = SOURCE.ACTIVE
  AND TARGET.OMS_CREATED_TSTMP = SOURCE.CREATED_DTTM
  AND TARGET.OMS_LAST_UPDATED_TSTMP = SOURCE.LAST_UPDATED_DTTM
  AND TARGET.UPDATE_TSTMP = SOURCE.UPDATE_TSTMP_exp
  AND TARGET.LOAD_TSTMP = SOURCE.LOAD_TSTMP_exp THEN DELETE
  WHEN NOT MATCHED
  AND SOURCE.UPDATE_STRATEGY_FLAG = "DD_INSERT" THEN
INSERT
  (
    TARGET.OMS_REASON_CODES_ID,
    TARGET.OMS_REASON_CODES_GROUP_ID,
    TARGET.OMS_REASON_CD,
    TARGET.OMS_REASON_TYPE,
    TARGET.COMMENTS,
    TARGET.REF_FIELD_1,
    TARGET.REF_FIELD_2,
    TARGET.ACTIVE_FLAG,
    TARGET.OMS_CREATED_TSTMP,
    TARGET.OMS_LAST_UPDATED_TSTMP,
    TARGET.UPDATE_TSTMP,
    TARGET.LOAD_TSTMP
  )
VALUES
  (
    SOURCE.REASON_CODES_ID,
    SOURCE.REASON_CODES_GROUP_ID,
    SOURCE.REASON_CODE,
    SOURCE.REASON_TYPE,
    SOURCE.COMMENTS,
    SOURCE.REF_FIELD_1,
    SOURCE.REF_FIELD_2,
    SOURCE.ACTIVE,
    SOURCE.CREATED_DTTM,
    SOURCE.LAST_UPDATED_DTTM,
    SOURCE.UPDATE_TSTMP_exp,
    SOURCE.LOAD_TSTMP_exp
  )""")

