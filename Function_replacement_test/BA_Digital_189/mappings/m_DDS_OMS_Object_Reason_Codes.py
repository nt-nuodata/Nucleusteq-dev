# DBTITLE 1, FTR_UNCHANGED_REC_5


query_5 = f"""SELECT
  REASON_CODES_ID AS REASON_CODES_ID,
  REASON_CODES_GROUP_ID AS REASON_CODES_GROUP_ID,
  REASON_CODE AS REASON_CODE,
  REASON_TYPE AS REASON_TYPE,
  COMMENTS AS COMMENTS,
  REF_FIELD_1 AS REF_FIELD_1,
  REF_FIELD_2 AS REF_FIELD_2,
  ACTIVE AS ACTIVE,
  CREATED_DTTM AS CREATED_DTTM,
  LAST_UPDATED_DTTM AS LAST_UPDATED_DTTM,
  lkp_OMS_REASON_CODES_ID AS lkp_OMS_REASON_CODES_ID,
  lkp_OMS_REASON_CODES_GROUP_ID AS lkp_OMS_REASON_CODES_GROUP_ID,
  lkp_OMS_REASON_CD AS lkp_OMS_REASON_CD,
  lkp_OMS_REASON_TYPE AS lkp_OMS_REASON_TYPE,
  lkp_COMMENTS AS lkp_COMMENTS,
  lkp_REF_FIELD_1 AS lkp_REF_FIELD_1,
  lkp_REF_FIELD_2 AS lkp_REF_FIELD_2,
  lkp_ACTIVE_FLAG AS lkp_ACTIVE_FLAG,
  lkp_OMS_CREATED_TSTMP AS lkp_OMS_CREATED_TSTMP,
  lkp_OMS_LAST_UPDATED_TSTMP AS lkp_OMS_LAST_UPDATED_TSTMP,
  lkp_LOAD_TSTMP AS lkp_LOAD_TSTMP,
  Monotonically_Increasing_Id AS Monotonically_Increasing_Id
FROM
  JNR_OMS_Object_Reason_Codes_4
WHERE
  ISNULL(lkp_OMS_REASON_CODES_ID)
  OR (
    NOT ISNULL(lkp_OMS_REASON_CODES_ID)
    AND (
      IFF(
        ISNULL(REASON_CODES_GROUP_ID),
        TO_INTEGER(999999999),
        REASON_CODES_GROUP_ID
      ) <> IFF(
        ISNULL(lkp_OMS_REASON_CODES_GROUP_ID),
        TO_INTEGER(999999999),
        lkp_OMS_REASON_CODES_GROUP_ID
      )
      OR IFF (
        ISNULL(LTRIM(RTRIM(REASON_CODE))),
        ' ',
        LTRIM(RTRIM(REASON_CODE))
      ) <> IFF(
        ISNULL(LTRIM(RTRIM(lkp_OMS_REASON_CD))),
        ' ',
        LTRIM(RTRIM(lkp_OMS_REASON_CD))
      )
      OR IFF (
        ISNULL(LTRIM(RTRIM(REASON_TYPE))),
        ' ',
        LTRIM(RTRIM(REASON_TYPE))
      ) <> IFF(
        ISNULL(LTRIM(RTRIM(lkp_OMS_REASON_TYPE))),
        ' ',
        LTRIM(RTRIM(lkp_OMS_REASON_TYPE))
      )
      OR IFF (
        ISNULL(LTRIM(RTRIM(COMMENTS))),
        ' ',
        LTRIM(RTRIM(COMMENTS))
      ) <> IFF(
        ISNULL(LTRIM(RTRIM(lkp_COMMENTS))),
        ' ',
        LTRIM(RTRIM(lkp_COMMENTS))
      )
      OR IFF (
        ISNULL(LTRIM(RTRIM(REF_FIELD_1))),
        ' ',
        LTRIM(RTRIM(REF_FIELD_1))
      ) <> IFF(
        ISNULL(LTRIM(RTRIM(lkp_REF_FIELD_1))),
        ' ',
        LTRIM(RTRIM(lkp_REF_FIELD_1))
      )
      OR IFF (
        ISNULL(LTRIM(RTRIM(REF_FIELD_2))),
        ' ',
        LTRIM(RTRIM(REF_FIELD_2))
      ) <> IFF(
        ISNULL(LTRIM(RTRIM(lkp_REF_FIELD_2))),
        ' ',
        LTRIM(RTRIM(lkp_REF_FIELD_2))
      )
      OR IFF(ISNULL(ACTIVE), TO_INTEGER(999999999), ACTIVE) <> IFF(
        ISNULL(lkp_ACTIVE_FLAG),
        TO_INTEGER(999999999),
        lkp_ACTIVE_FLAG
      )
      OR IFF(
        ISNULL(CREATED_DTTM),
        To_DATE('12-31-9999', 'MM-DD-YYYY'),
        CREATED_DTTM
      ) <> IFF(
        ISNULL(lkp_OMS_CREATED_TSTMP),
        To_DATE('12-31-9999', 'MM-DD-YYYY'),
        lkp_OMS_CREATED_TSTMP
      )
      OR IFF(
        ISNULL(LAST_UPDATED_DTTM),
        To_DATE('12-31-9999', 'MM-DD-YYYY'),
        LAST_UPDATED_DTTM
      ) <> IFF(
        ISNULL(lkp_OMS_LAST_UPDATED_TSTMP),
        To_DATE('12-31-9999', 'MM-DD-YYYY'),
        lkp_OMS_LAST_UPDATED_TSTMP
      )
    )
  )"""

df_5 = spark.sql(query_5)

df_5.createOrReplaceTempView("FTR_UNCHANGED_REC_5")

