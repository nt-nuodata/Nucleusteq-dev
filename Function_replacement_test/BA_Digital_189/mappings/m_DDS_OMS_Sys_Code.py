query_5 = f"""SELECT
  REC_TYPE AS REC_TYPE,
  CODE_TYPE AS CODE_TYPE,
  CODE_ID AS CODE_ID,
  CODE_DESC AS CODE_DESC,
  SHORT_DESC AS SHORT_DESC,
  CREATE_DATE_TIME AS CREATE_DATE_TIME,
  MOD_DATE_TIME AS MOD_DATE_TIME,
  SYS_CODE_ID AS SYS_CODE_ID,
  SYS_CODE_TYPE_ID AS SYS_CODE_TYPE_ID,
  CREATED_DTTM AS CREATED_DTTM,
  LAST_UPDATED_DTTM AS LAST_UPDATED_DTTM,
  lkp_OMS_REC_TYPE AS lkp_OMS_REC_TYPE,
  lkp_OMS_CODE_TYPE AS lkp_OMS_CODE_TYPE,
  lkp_OMS_CODE_ID AS lkp_OMS_CODE_ID,
  lkp_OMS_CODE_DESC AS lkp_OMS_CODE_DESC,
  lkp_OMS_CODE_SHORT_DESC AS lkp_OMS_CODE_SHORT_DESC,
  lkp_OMS_SYS_CODE_ID AS lkp_OMS_SYS_CODE_ID,
  lkp_OMS_SYS_CODE_TYPE_ID AS lkp_OMS_SYS_CODE_TYPE_ID,
  lkp_OMS_CREATE_TSTMP AS lkp_OMS_CREATE_TSTMP,
  lkp_OMS_MOD_TSTMP AS lkp_OMS_MOD_TSTMP,
  lkp_OMS_CREATED_TSTMP AS lkp_OMS_CREATED_TSTMP,
  lkp_OMS_LAST_UPDATED_TSTMP AS lkp_OMS_LAST_UPDATED_TSTMP,
  lkp_LOAD_TSTMP AS lkp_LOAD_TSTMP,
  Monotonically_Increasing_Id AS Monotonically_Increasing_Id
FROM
  JNR_SYS_CODE_4
WHERE
  ISNULL(lkp_OMS_REC_TYPE)
  OR (
    NOT ISNULL(lkp_OMS_REC_TYPE)
    AND (
      IFF (
        ISNULL(LTRIM(RTRIM(CODE_DESC))),
        ' ',
        LTRIM(RTRIM(CODE_DESC))
      ) <> IFF(
        ISNULL(LTRIM(RTRIM(lkp_OMS_CODE_DESC))),
        ' ',
        LTRIM(RTRIM(lkp_OMS_CODE_DESC))
      )
      OR IFF (
        ISNULL(LTRIM(RTRIM(SHORT_DESC))),
        ' ',
        LTRIM(RTRIM(SHORT_DESC))
      ) <> IFF(
        ISNULL(LTRIM(RTRIM(lkp_OMS_CODE_SHORT_DESC))),
        ' ',
        LTRIM(RTRIM(lkp_OMS_CODE_SHORT_DESC))
      )
      OR IFF(
        ISNULL(SYS_CODE_ID),
        TO_INTEGER(999999999),
        SYS_CODE_ID
      ) <> IFF(
        ISNULL(lkp_OMS_SYS_CODE_ID),
        TO_INTEGER(999999999),
        lkp_OMS_SYS_CODE_ID
      )
      OR IFF(
        ISNULL(SYS_CODE_TYPE_ID),
        TO_INTEGER(999999999),
        SYS_CODE_TYPE_ID
      ) <> IFF(
        ISNULL(lkp_OMS_SYS_CODE_TYPE_ID),
        TO_INTEGER(999999999),
        lkp_OMS_SYS_CODE_TYPE_ID
      )
      OR IFF(
        ISNULL(CREATED_DTTM),
        To_DATE('12-31-9999', 'MM-DD-YYYY'),
        CREATED_DTTM
      ) <> IFF(
        ISNULL(lkp_OMS_CREATED_TSTMP),
        To_DATE('12-31-9999', 'MM-DD-YYYY'),
        lkp_OMS_CREATED_TSTMP
      )
      OR IFF(
        ISNULL(LAST_UPDATED_DTTM),
        To_DATE('12-31-9999', 'MM-DD-YYYY'),
        LAST_UPDATED_DTTM
      ) <> IFF(
        ISNULL(lkp_OMS_LAST_UPDATED_TSTMP),
        To_DATE('12-31-9999', 'MM-DD-YYYY'),
        lkp_OMS_LAST_UPDATED_TSTMP
      )
      OR IFF(
        ISNULL(CREATE_DATE_TIME),
        To_DATE('12-31-9999', 'MM-DD-YYYY'),
        CREATE_DATE_TIME
      ) <> IFF(
        ISNULL(lkp_OMS_CREATE_TSTMP),
        To_DATE('12-31-9999', 'MM-DD-YYYY'),
        lkp_OMS_CREATE_TSTMP
      )
      OR IFF(
        ISNULL(MOD_DATE_TIME),
        To_DATE('12-31-9999', 'MM-DD-YYYY'),
        MOD_DATE_TIME
      ) <> IFF(
        ISNULL(lkp_OMS_MOD_TSTMP),
        To_DATE('12-31-9999', 'MM-DD-YYYY'),
        lkp_OMS_MOD_TSTMP
      )
    )
  )"""

df_5 = spark.sql(query_5)

df_5.createOrReplaceTempView("FTR_UNCHANGED_REC_5")

