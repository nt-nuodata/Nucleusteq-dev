spark.sql("""MERGE INTO OMS_SYS_CODE AS TARGET
USING
  UPD_INS_UPD_7 AS SOURCE ON TARGET.OMS_CODE_TYPE = SOURCE.CODE_TYPE
  AND TARGET.OMS_REC_TYPE = SOURCE.REC_TYPE
  AND TARGET.OMS_CODE_ID = SOURCE.CODE_ID
  WHEN MATCHED
  AND SOURCE.UPDATE_STRATEGY_FLAG = "DD_UPDATE" THEN
UPDATE
SET
  TARGET.OMS_REC_TYPE = SOURCE.REC_TYPE,
  TARGET.OMS_CODE_TYPE = SOURCE.CODE_TYPE,
  TARGET.OMS_CODE_ID = SOURCE.CODE_ID,
  TARGET.OMS_CODE_DESC = SOURCE.CODE_DESC,
  TARGET.OMS_CODE_SHORT_DESC = SOURCE.SHORT_DESC,
  TARGET.OMS_SYS_CODE_ID = SOURCE.SYS_CODE_ID,
  TARGET.OMS_SYS_CODE_TYPE_ID = SOURCE.SYS_CODE_TYPE_ID,
  TARGET.OMS_CREATE_TSTMP = SOURCE.CREATE_DATE_TIME,
  TARGET.OMS_MOD_TSTMP = SOURCE.MOD_DATE_TIME,
  TARGET.OMS_CREATED_TSTMP = SOURCE.CREATED_DTTM,
  TARGET.OMS_LAST_UPDATED_TSTMP = SOURCE.LAST_UPDATED_DTTM,
  TARGET.UPDATE_TSTMP = SOURCE.UPDATE_TSTMP,
  TARGET.LOAD_TSTMP = SOURCE.LOAD_TSTMP_exp
  WHEN MATCHED
  AND SOURCE.UPDATE_STRATEGY_FLAG = "DD_DELETE"
  AND TARGET.OMS_CODE_DESC = SOURCE.CODE_DESC
  AND TARGET.OMS_CODE_SHORT_DESC = SOURCE.SHORT_DESC
  AND TARGET.OMS_SYS_CODE_ID = SOURCE.SYS_CODE_ID
  AND TARGET.OMS_SYS_CODE_TYPE_ID = SOURCE.SYS_CODE_TYPE_ID
  AND TARGET.OMS_CREATE_TSTMP = SOURCE.CREATE_DATE_TIME
  AND TARGET.OMS_MOD_TSTMP = SOURCE.MOD_DATE_TIME
  AND TARGET.OMS_CREATED_TSTMP = SOURCE.CREATED_DTTM
  AND TARGET.OMS_LAST_UPDATED_TSTMP = SOURCE.LAST_UPDATED_DTTM
  AND TARGET.UPDATE_TSTMP = SOURCE.UPDATE_TSTMP
  AND TARGET.LOAD_TSTMP = SOURCE.LOAD_TSTMP_exp THEN DELETE
  WHEN NOT MATCHED
  AND SOURCE.UPDATE_STRATEGY_FLAG = "DD_INSERT" THEN
INSERT
  (
    TARGET.OMS_REC_TYPE,
    TARGET.OMS_CODE_TYPE,
    TARGET.OMS_CODE_ID,
    TARGET.OMS_CODE_DESC,
    TARGET.OMS_CODE_SHORT_DESC,
    TARGET.OMS_SYS_CODE_ID,
    TARGET.OMS_SYS_CODE_TYPE_ID,
    TARGET.OMS_CREATE_TSTMP,
    TARGET.OMS_MOD_TSTMP,
    TARGET.OMS_CREATED_TSTMP,
    TARGET.OMS_LAST_UPDATED_TSTMP,
    TARGET.UPDATE_TSTMP,
    TARGET.LOAD_TSTMP
  )
VALUES
  (
    SOURCE.REC_TYPE,
    SOURCE.CODE_TYPE,
    SOURCE.CODE_ID,
    SOURCE.CODE_DESC,
    SOURCE.SHORT_DESC,
    SOURCE.SYS_CODE_ID,
    SOURCE.SYS_CODE_TYPE_ID,
    SOURCE.CREATE_DATE_TIME,
    SOURCE.MOD_DATE_TIME,
    SOURCE.CREATED_DTTM,
    SOURCE.LAST_UPDATED_DTTM,
    SOURCE.UPDATE_TSTMP,
    SOURCE.LOAD_TSTMP_exp
  )""")

